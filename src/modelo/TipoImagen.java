
package modelo;

// Enumeración que representa los diferentes tipos de imágenes que se pueden utilizar en el juego
public enum TipoImagen {
    EQUIS,   // Representa la imagen de la "X" en el juego
    CIRCULO, // Representa la imagen del "Círculo" en el juego
    LINEA1,  // Representa la primera línea de victoria (por ejemplo, horizontal o vertical)
    LINEA2,  // Representa la segunda línea de victoria
    LINEA3,  // Representa la tercera línea de victoria
    LINEA4,  // Representa la cuarta línea de victoria
    LINEA5,  // Representa la quinta línea de victoria
    LINEA6,  // Representa la sexta línea de victoria
    LINEA7,  // Representa la séptima línea de victoria
    LINEA8,  // Representa la octava línea de victoria
    EMPATE   // Representa el resultado de empate en el juego
}
